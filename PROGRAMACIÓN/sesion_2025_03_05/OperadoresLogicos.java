package sesion_2025_03_05;

public class OperadoresLogicos {

    public static void main(String[] args) {

        boolean a = true, b = false;

        System.out.println("a AND b: " + (a && b));
        System.out.println("a OR b: " + (a || b));
        System.out.println("NOT a: " + (!a));
    }

}
